package codingexample;

public class Example1 {
    public static void main(String[] args) {

        char operator = '/';
        int num1=74;
        int num2=10;

        if (operator=='+') {
            System.out.println(num1+num2);
        } else if (operator=='+') {
            System.out.println(num1+num2);
        } else if (operator=='-') {
            System.out.println(num1-num2);
        } else if (operator=='*') {
            System.out.println(num1*num2);
        } else if (operator=='/') {
            System.out.println(num1/num2);
        } else {
            System.out.println("Error");
        }
    }
}
